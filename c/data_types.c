#import <stdio.h>

int main() {
	// characters in c 
	char number = '3';
	char letter = 'a';
	char bracket = ']';

	printf("Number: %c, Letter: %c, Bracket: %c\n", number, letter, bracket);

	// numbers in c
	int value = 6;
	float floatNumber = 50.0;

	// all carrage return for cleanlyness 
	printf("Value: %d, Floating Number: %.1f\n", value, floatNumber);

	return 0;
}
